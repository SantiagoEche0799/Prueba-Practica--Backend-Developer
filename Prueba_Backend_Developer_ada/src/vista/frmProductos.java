    private void jTable_ProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ProductosMouseClicked

        PreparedStatement ps = null;
        ResultSet rs = null;
        try {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();

            int Fila = jTable_Productos.getSelectedRow();
            String codigo = jTable_Productos.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT codigo, nombre, marca, fecha_Vencimiento, "
                    + "costo, cantidad FROM Productos WHERE codigo=?");
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                jTextField_Codigo.setText(rs.getString("codigo"));
                jTextField_Nombre.setText(rs.getString("nombre"));
                jTextField_Marca.setText(rs.getString("marca"));
                jTextField_FechaVenci.setText(rs.getString("fecha_Vencimiento"));
                jTextField_Costo.setText(rs.getString("costo"));
                jTextField_Canti.setText(rs.getString("cantidad"));
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }


    }//GEN-LAST:event_jTable_ProductosMouseClicked

