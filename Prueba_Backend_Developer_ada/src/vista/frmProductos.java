    public frmProductos() {
        initComponents();

        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTable_Productos.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConexion();

            String sql = "SELECT codigo, nombre, marca, fecha_Vencimiento, costo,"
                    + "cantidad FROM Productos";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Código");
            modelo.addColumn("Nombre");
            modelo.addColumn("Marca");
            modelo.addColumn("Fecha de Vencimiento");
            modelo.addColumn("Costo");
            modelo.addColumn("Cantidad");
            int[] anchos = {50, 100, 100, 100, 100, 100};
            for (int i = 0; i < anchos.length; i++) {
                jTable_Productos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }

    }

